#include "libft.h"

int	ft_count_nb(int nb)
{
	int	count;

	count = 1;
	if (nb == -2147483648)
	{
		count += 1;
	}
	else if (nb <= -1)
	{
		nb *= -1;
		count += 1;
	}
	while (nb >= 10)
	{
		nb /= 10;
		count++;
	}
	return (count);
}
